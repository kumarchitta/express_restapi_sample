class  Student{
    constructor(student_id,first_name,last_name,age,email,major,college,gpa,graduation_date){
      this.student_id = student_id;
      this.first_name = first_name;
      this.last_name = last_name;
      this.age = age;
      this.email = email;
      this.major = major;
      this.college = college;
      this.gpa = gpa;
      this.graduation_date = graduation_date;
    }
}

module.exports = Student;
  